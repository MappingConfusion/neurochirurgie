document.addEventListener('DOMContentLoaded', () => {
  const header = document.querySelector('.site-header');
  const navToggle = document.querySelector('.nav-toggle');
  const navMenu = document.querySelector('.nav-menu');
  const navLinks = document.querySelectorAll('.nav-link');
  const sections = document.querySelectorAll('section[id]');
  const langButtons = document.querySelectorAll('.lang-btn');
  const urlParams = new URLSearchParams(window.location.search);
  const savedLang = urlParams.get('lang') || localStorage.getItem('preferred-lang') || 'en';

  function setLanguage(lang, updateUrl = true) {
    if (!translations[lang]) return;
    const t = translations[lang];

    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('data-lang', lang);

    // Title
    const titleEl = document.querySelector('title[data-i18n]');
    if (titleEl) document.title = t[titleEl.getAttribute('data-i18n')];

    // Meta description
    const metaDesc = document.querySelector('meta[data-i18n="meta_description"]');
    if (metaDesc) metaDesc.setAttribute('content', t.meta_description);

    // Text nodes
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key] !== undefined && key !== 'meta_description') {
        el.textContent = t[key];
      }
    });

    // HTML content
    document.querySelectorAll('[data-i18n_html]').forEach(el => {
      const key = el.getAttribute('data-i18n_html');
      if (t[key] !== undefined) {
        el.innerHTML = t[key];
      }
    });

    // Update language buttons
    langButtons.forEach(btn => {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    if (updateUrl) {
      const params = new URLSearchParams(window.location.search);
      params.set('lang', lang);
      history.replaceState(null, '', `${window.location.pathname}?${params.toString()}`);
    }

    localStorage.setItem('preferred-lang', lang);
  }

  langButtons.forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.getAttribute('data-lang'), true));
  });

  setLanguage(savedLang);

  // Sticky header shadow
  function onScroll() {
    if (window.scrollY > 20) {
      header.classList.add('scrolled');
    } else {
      header.classList.remove('scrolled');
    }
    highlightActiveLink();
  }

  // Mobile menu toggle
  function toggleMenu() {
    const isOpen = navMenu.classList.toggle('open');
    navToggle.setAttribute('aria-expanded', String(isOpen));
  }

  function closeMenu() {
    navMenu.classList.remove('open');
    navToggle.setAttribute('aria-expanded', 'false');
  }

  navToggle.addEventListener('click', toggleMenu);

  navLinks.forEach(link => {
    link.addEventListener('click', () => {
      if (navMenu.classList.contains('open')) {
        closeMenu();
      }
    });
  });

  // Active section highlighting
  function highlightActiveLink() {
    let current = '';
    const scrollPos = window.scrollY + 120;

    sections.forEach(section => {
      const top = section.offsetTop;
      const height = section.offsetHeight;
      if (scrollPos >= top && scrollPos < top + height) {
        current = section.getAttribute('id');
      }
    });

    navLinks.forEach(link => {
      link.classList.remove('active');
      if (link.getAttribute('href') === `#${current}`) {
        link.classList.add('active');
      }
    });
  }

  window.addEventListener('scroll', onScroll, { passive: true });
  onScroll();
});
